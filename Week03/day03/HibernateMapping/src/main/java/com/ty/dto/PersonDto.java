package com.ty.dto;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToOne;

@Entity
public class PersonDto {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY) //only for primary key ( give value automatically)
	private int id;
	private String fname;
	private String lname;
	private long phone;
	private String location;
	
//	Creating mapping between two tables
	@OneToOne
	private AadharDto aadhar;

	public AadharDto getAadhar() {
		return aadhar;
	}

	public void setAadhar(AadharDto aadhar) {
		this.aadhar = aadhar;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getLname() {
		return lname;
	}

	public void setLname(String lname) {
		this.lname = lname;
	}

	public long getPhone() {
		return phone;
	}

	public void setPhone(long phone) {
		this.phone = phone;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	private String gender;

}
